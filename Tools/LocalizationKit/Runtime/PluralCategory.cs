namespace YokiFrame
{
    /// <summary>
    /// 表示本地化复数文本分类。
    /// </summary>
    public enum PluralCategory
    {
        /// <summary>
        /// 零数量分类。
        /// </summary>
        Zero = 0,

        /// <summary>
        /// 单数分类。
        /// </summary>
        One = 1,

        /// <summary>
        /// 双数分类。
        /// </summary>
        Two = 2,

        /// <summary>
        /// 少量分类。
        /// </summary>
        Few = 3,

        /// <summary>
        /// 大量分类。
        /// </summary>
        Many = 4,

        /// <summary>
        /// 默认或其它分类。
        /// </summary>
        Other = 5
    }
}
