        private static void AppendTableKitLoadMethods(CodeGenLineBuilder sb)
        {
            sb.AppendLine();
            sb.AppendLine("    private static MethodInfo sJsonParseMethod;");
            sb.AppendLine();
            sb.AppendLine("    private static object CreateJsonLoader(Type jsonNodeType)");
            sb.AppendLine("    {");
            sb.AppendLine("        var funcType = typeof(Func<,>).MakeGenericType(typeof(string), jsonNodeType);");
            sb.AppendLine("        var loadJsonMethod = typeof(TableKit).GetMethod(nameof(LoadJsonDynamic),");
            sb.AppendLine("            BindingFlags.NonPublic | BindingFlags.Static);");
            sb.AppendLine("        return Delegate.CreateDelegate(funcType, loadJsonMethod);");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    private static object LoadJsonDynamic(string fileName)");
            sb.AppendLine("    {");
            sb.AppendLine("        var json = sJsonLoader(fileName);");
            sb.AppendLine("        if (string.IsNullOrEmpty(json))");
            sb.AppendLine("        {");
            sb.AppendLine("            YokiFrame.LogKit.Error($\"[TableKit] 加载配置表失败: {fileName}\");");
            sb.AppendLine("            return null;");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        if (sJsonParseMethod == null)");
            sb.AppendLine("        {");
            sb.AppendLine("            var jsonType = GetLoadedAssemblies()");
            sb.AppendLine("                .Where(a => !a.IsDynamic)");
            sb.AppendLine("                .SelectMany(a => { try { return a.GetTypes(); } catch { return Type.EmptyTypes; } })");
            sb.AppendLine("                .FirstOrDefault(t => t.FullName == \"Luban.SimpleJson.JSON\" || t.FullName == \"SimpleJSON.JSON\");");
            sb.AppendLine();
            sb.AppendLine("            if (jsonType == null)");
            sb.AppendLine("                throw new InvalidOperationException(\"无法找到 JSON 类型\");");
            sb.AppendLine();
            sb.AppendLine("            sJsonParseMethod = jsonType.GetMethod(\"Parse\", BindingFlags.Public | BindingFlags.Static,");
            sb.AppendLine("                null, new[] { typeof(string) }, null);");
            sb.AppendLine("        }");
            sb.AppendLine();
            sb.AppendLine("        return sJsonParseMethod.Invoke(null, new object[] { json });");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    private static ByteBuf LoadBinary(string fileName)");
            sb.AppendLine("    {");
            sb.AppendLine("        var bytes = sBinaryLoader(fileName);");
            sb.AppendLine("        if (bytes == null || bytes.Length == 0)");
            sb.AppendLine("        {");
            sb.AppendLine("            YokiFrame.LogKit.Error($\"[TableKit] 加载配置表失败: {fileName}\");");
            sb.AppendLine("            return null;");
            sb.AppendLine("        }");
            sb.AppendLine("        return new ByteBuf(bytes);");
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    private static Assembly[] GetLoadedAssemblies()");
            sb.AppendLine("    {");
            sb.AppendLine("#if UNITY_6000_5_OR_NEWER && !GODOT");
            sb.AppendLine("        return CurrentAssemblies.GetLoadedAssemblies();");
            sb.AppendLine("#else");
            sb.AppendLine("        return AppDomain.CurrentDomain.GetAssemblies();");
            sb.AppendLine("#endif");
            sb.AppendLine("    }");
        }

        private static void AppendDefaultLoaders(CodeGenLineBuilder sb, bool useRawResourceLoading)
        {
            sb.AppendLine();
            sb.AppendLine("    #region Default Loaders");
            sb.AppendLine();

            sb.AppendLine("    // 默认加载器：统一委托给 ResKit，具体 Unity/Godot/自定义后端由 ResKit Provider 决定。");
            sb.AppendLine("    private static byte[] DefaultBinaryLoader(string fileName)");
            sb.AppendLine("    {");
            sb.AppendLine("        var path = string.Format(RuntimePathPattern, fileName);");
            if (useRawResourceLoading)
            {
                sb.AppendLine("        return YokiFrame.ResKit.LoadRaw(path);");
            }
            else
            {
                sb.AppendLine("        var asset = YokiFrame.ResKit.Load<UnityEngine.TextAsset>(path);");
                sb.AppendLine("        if (asset == null) return null;");
                sb.AppendLine("        try");
                sb.AppendLine("        {");
                sb.AppendLine("            return asset.bytes;");
                sb.AppendLine("        }");
                sb.AppendLine("        finally");
                sb.AppendLine("        {");
                sb.AppendLine("            YokiFrame.ResKit.Release(asset);");
                sb.AppendLine("        }");
            }
            sb.AppendLine("    }");
            sb.AppendLine();
            sb.AppendLine("    private static string DefaultJsonLoader(string fileName)");
            sb.AppendLine("    {");
            sb.AppendLine("        var path = string.Format(RuntimePathPattern, fileName);");
            if (useRawResourceLoading)
            {
                sb.AppendLine("        return YokiFrame.ResKit.LoadRawText(path);");
            }
            else
            {
                sb.AppendLine("        var asset = YokiFrame.ResKit.Load<UnityEngine.TextAsset>(path);");
                sb.AppendLine("        if (asset == null) return null;");
                sb.AppendLine("        try");
                sb.AppendLine("        {");
                sb.AppendLine("            return asset.text;");
                sb.AppendLine("        }");
                sb.AppendLine("        finally");
                sb.AppendLine("        {");
                sb.AppendLine("            YokiFrame.ResKit.Release(asset);");
                sb.AppendLine("        }");
            }
            sb.AppendLine("    }");
            
            sb.AppendLine();
            sb.AppendLine("    #endregion");
        }

        private static void AppendTableKitReloadAndClear(CodeGenLineBuilder sb, bool useAsyncLoading)
        {
            sb.AppendLine();
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// Reloads all table data.");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    public static void Reload(Action onComplete = null)");
            sb.AppendLine("    {");
            sb.AppendLine("        try");
            sb.AppendLine("        {");
            sb.AppendLine("            sTables = null;");
            sb.AppendLine("            Initialized = false;");
            sb.AppendLine("            Init();");
            sb.AppendLine("        }");
            sb.AppendLine("        catch (Exception ex)");
            sb.AppendLine("        {");
            sb.AppendLine("            YokiFrame.LogKit.Error($\"[TableKit] Reload failed: {ex.Message}\");");
            sb.AppendLine("        }");
            sb.AppendLine("        onComplete?.Invoke();");
            sb.AppendLine("    }");

            if (useAsyncLoading)
            {
                sb.AppendLine();
                sb.AppendLine("#if YOKIFRAME_UNITASK_SUPPORT");
                sb.AppendLine("    /// <summary>");
                sb.AppendLine("    /// Reloads all table data asynchronously.");
                sb.AppendLine("    /// </summary>");
                sb.AppendLine("    public static async UniTask ReloadAsync(CancellationToken cancellationToken = default)");
                sb.AppendLine("    {");
                sb.AppendLine("        sTables = null;");
                sb.AppendLine("        Initialized = false;");
                sb.AppendLine("        await InitAsync(cancellationToken);");
                sb.AppendLine("    }");
                sb.AppendLine("#endif");
            }

            sb.AppendLine();
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// Clears all loaded table data.");
            sb.AppendLine("    /// </summary>");
            sb.AppendLine("    public static void Clear()");
            sb.AppendLine("    {");
            sb.AppendLine("        sTables = null;");
            sb.AppendLine("        Initialized = false;");
            sb.AppendLine("#if UNITY_EDITOR");
            sb.AppendLine("        sTablesEditor = null;");
            sb.AppendLine("#endif");
            sb.AppendLine("    }");
        }

        private static void AppendTableKitEditorSection(CodeGenLineBuilder sb, string tablesNamespace, string escapedEditorPath)
        {
            sb.AppendLine();
            sb.AppendLine("#if UNITY_EDITOR");
            sb.AppendLine($"    private static {tablesNamespace}.Tables sTablesEditor;");
            sb.AppendLine();
            sb.AppendLine("    /// <summary>");
            sb.AppendLine("    /// 编辑器数据路径（生成时嵌入）");
            sb.AppendLine("    /// </summary>");
